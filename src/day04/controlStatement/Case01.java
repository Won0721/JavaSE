package day04.controlStatement;

public class Case01 {
	public static void main(String[] args) {
		int num=1;
		
		switch(num) {
		case 0 :
			System.out.println("num 은 0 입니다");
			break;
		case 1 :
			System.out.println("num 은 1 입니다");
			break;
		case 2 :
			System.out.println("num 은 2 입니다");
			break;
		case 3 :
			System.out.println("num 은 3 입니다");
			break;
		default:
			System.out.println("num 은 0~3 사이가 아닙니다.");
			break;
			
			
		}
	}

}
